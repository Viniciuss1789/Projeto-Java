package exemplos;

public class TiposDados {
	
	public static void main(String[] args) {
		
		byte numero1 = -128;
		byte numero2 = 127;
		short numero3 = 32767;
		short numero4 = -32768;
		int numero5 = 12;
		float numero6 = 12.34f;
		double numero7 = 123.456d;
		long numero8 = 9223372036854775807l;
		long numero9 = -9223372036854775808l;
		String nome = "Vinicius";
		
		System.out.println("byte: " + numero1);
		System.out.println("byte: " + numero2);
		System.out.println("short: " + numero3);
		System.out.println("short: " + numero4);
		System.out.println("int: " + numero5);
		System.out.println("float: " + numero6);
		System.out.println("double: " + numero7);
		System.out.println("long: " + numero8);
		System.out.println("long: " + numero9);
		System.out.println("String: " + nome);
		
	}

}
